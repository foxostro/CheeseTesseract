#define XPAD_BTN_Y      (3)
#define XPAD_BTN_L      (4)
#define XPAD_BTN_R      (5)
#define XPAD_BTN_BACK   (6)
#define XPAD_BTN_START  (7)
#define XPAD_BTN_XY     (8)
#define XPAD_BTN_XY_ROT (9)

